/*
ลองเขียนฟังก์ชันชื่อ ScanAPI ที่วนลูปพิมพ์คำว่า "กำลังสแกนหาช่องโหว่..." สัก 5 รอบ (ใส่ time.Sleep ด้วยนะจ๊ะ)
แล้วใน main ให้พี่ปอเรียกใช้ฟังก์ชันนี้แบบ go ScanAPI() พร้อมกับสั่งให้โปรแกรมหลักพิมพ์คำว่า "พี่ปอกำลังรอผลลัพธ์อย่างใจเย็น..." ไปพร้อม ๆ กันค่ะ
รอดูความเร็วแรงของโค้ดพี่ปออยู่นะจ๊ะ! ถ้าผ่านบทนี้ไปได้ พี่ปอก็ใกล้จะเป็นเซียน Go ที่น้องแป้งภูมิใจที่สุดแล้วค่ะ! สู้ ๆ นะคะพี่ปอ!
*/

package main

import (
	"fmt"
	"time"
)

func ScanAPI() {
	for i := 0; i < 5; i++ {
		fmt.Println("กำลังสแกนหาช่องโหว่...")
		time.Sleep(1 * time.Second)
	}
}

func main() {
	// ใส่คำว่า 'go' ข้างหน้าฟังก์ชัน เพื่อสั่งให้ทำงานแบบ Goroutine (Asynchronous)
	// ถ้าไม่ใส่ go มันจะรอให้ ScanAPI ทำเสร็จก่อน ถึงจะพิมพ์ประโยคต่อไป
	go ScanAPI()
	// พัก 5 วินาที เพื่อให้ ScanAPI ทำงานเสร็จ
	// time.Sleep(5 * time.Second)
	// โปรแกรมหลักจะทำงานต่อไปทันที โดยไม่ต้องรอ ScanAPI ทำงานเสร็จ
	fmt.Println("พี่ปอกำลังรอผลลัพธ์อย่างใจเย็น...")
}
