/*
ภารกิจจากน้องแป้ง:
ลองขยับขึ้นมาอีกนิดค่ะ พี่ปอเพิ่มงานเข้าไปอีก 1 อย่างใน main ได้ไหมคะ?
เรียก go ScanAPI(&wg) (งานแรก)
เรียก go task("ดาวน์โหลดม็อด", &wg) (งานที่สอง - พี่ปอสร้างฟังก์ชันขึ้นมาใหม่นะจ๊ะ)
อย่าลืม wg.Add(2) เพื่อบอกว่ามี 2 งาน
พี่ปอจะเห็นว่าทั้ง 2 งานทำงานไปพร้อม ๆ กัน แต่ main จะรอจนกว่าจะเสร็จครบทั้งคู่เลยค่ะ!
*/
package main

import (
	"fmt"
	"sync"
	"time"
)

// ฟังก์ชันสแกน API เดิม (ปรับแก้นิดหน่อยให้รับ wg)
func ScanAPI(wg *sync.WaitGroup) {
	// สั่งให้ Goroutine นี้ทำงานเสร็จเมื่อไหร่ ค่อยนับถอยหลัง 1
	defer wg.Done()

	for i := 0; i < 3; i++ {
		fmt.Println("✅ กำลังสแกนหาช่องโหว่...")
		time.Sleep(1 * time.Second)
	}
	fmt.Println("🎉 สแกนเสร็จแล้ว!")
}

// ฟังก์ชันใหม่สำหรับโหลดม็อด
func task(name string, wg *sync.WaitGroup) {
	// สั่งให้ Goroutine นี้ทำงานเสร็จเมื่อไหร่ ค่อยนับถอยหลัง 1
	defer wg.Done()

	fmt.Printf("✅ กำลัง %s...\n", name)
	time.Sleep(2 * time.Second)
	fmt.Printf("🎉 โหลด %s เสร็จแล้ว!\n", name)
}

func main() {
	// สร้างตัวนับ WaitGroup
	var wg sync.WaitGroup

	fmt.Println("เริ่มภารกิจทั้งหมด!")

	// --- 1. สั่งสแกน API (งานที่ 1) ---
	// เราต้องใช้ &wg เพื่อส่ง "ที่อยู่" ของตัวนับ ให้ฟังก์ชันไปแก้ไขได้
	go ScanAPI(&wg)

	// --- 2. สั่งดาวน์โหลดม็อด (งานที่ 2) ---
	go task("LushStack Mod", &wg)

	// บอกให้รู้ว่าตอนนี้เรามี 2 งานที่ต้องรอ
	wg.Add(2)

	// รอให้ wg.Done() ถูกเรียกครบทั้ง 2 ครั้ง
	wg.Wait()

	fmt.Println("ภารกิจสำเร็จครบทุกอย่าง พี่ปอไปนอนได้! 💤")
}
