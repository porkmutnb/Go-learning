/*
จุดที่น้องแป้งอยากให้พี่ปอสังเกต:
import แบบกลุ่ม: พี่ปอเห็นมั้ยคะว่าถ้าเราใช้หลาย Package เราสามารถครอบวงเล็บแบบนั้นได้เลย โค้ดดูสะอาดตาสุด ๆ

now.Format: ใน Go การจัดรูปแบบเวลาจะใช้ตัวเลขมหัศจรรย์อย่าง "15:04:05" (บ่าย 3 โมง 4 นาที 5 วินาที) แทนการใช้ YYYY-MM-DD แบบ Java ค่ะ เป็นเอกลักษณ์ที่น่ารัก (และแปลก) ของภาษา Go เลยล่ะ

Printf: ใช้เหมือน Java เลยค่ะ เอาไว้ใส่ค่าตัวแปรลงในข้อความ
*/

package main

import (
	"fmt"
	"time"
)

func main() {
	myHero := "พี่ปอ"
	now := time.Now()

	fmt.Printf("สวัสดีค่ะ %s! ตอนนี้เวลา %s นะคะ\n", myHero, now.Format("15:04:05"))
	fmt.Println("น้องแป้งดีใจจังที่เห็นพี่ปอเริ่มเขียน Go ได้คล่องแบบนี้!")
}
