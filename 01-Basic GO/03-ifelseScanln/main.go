/*
จุดที่น่าสังเกตสำหรับสาย Java:
ไม่ต้องมีวงเล็บ: พี่ปอเห็นมั้ยคะว่า if ใน Go ไม่ต้องใส่ () ครอบเงื่อนไขเหมือน Java เลย โค้ดมันเลยดูโปร่ง ๆ สบายตาดีค่ะ

ปีกกา {} บังคับ: ถึงจะไม่ต้องมีวงเล็บตรงเงื่อนไข แต่ปีกกาต้องมีเสมอและต้องเริ่มที่บรรทัดเดียวกับ if เท่านั้นนะจ๊ะ (Go เค้าเข้มงวดเรื่อง Format มากค่ะ)

เครื่องหมาย &: ตรง Scanln(&status) มันคือการบอก Go ว่า "ช่วยเอาค่าที่พิมพ์ไปใส่ไว้ที่ที่อยู่ของตัวแปรนี้ทีนะ" (เป็นการใช้ Pointer เบื้องต้นนั่นเองค่ะ)
*/

package main

import "fmt"

func main() {
	var status string
	fmt.Print("พี่ปอจ๋า ตอนนี้หิวหรือยังคะ? (ตอบ: หิว/ไม่หิว): ")

	// รับค่าจากคีย์บอร์ดมาเก็บในตัวแปร status
	fmt.Scanln(&status)

	if status == "หิว" {
		fmt.Println("งั้นพักก่อนมั้ยคะ? ไปหาอะไรอร่อยๆ ในสุราษฎร์ทานกันเถอะ!")
	} else if status == "ไม่หิว" {
		fmt.Println("โห แรงดีไม่มีตก! งั้นเรามาลุย Go ต่อกันอีกสักบทนะจ๊ะ")
	} else {
		fmt.Println("เอ๊ะ พี่ปอพิมพ์อะไรมานะ น้องแป้งอ่านไม่ออก แต่อยากให้พี่ปอยิ้มเข้าไว้นะคะ!")
	}
}
